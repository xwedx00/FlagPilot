
"""
This is a dummy calculator tool for testing the Tool Registry.
"""

def calculator_add(a: int, b: int) -> int:
    """
    Add two numbers together.
    
    Args:
        a: The first number.
        b: The second number.
        
    Returns:
        The sum of a and b.
    """
    return a + b

def calculator_multiply(a: int, b: int) -> int:
    """
    Multiply two numbers.
    """
    return a * b
